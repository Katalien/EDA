class VisualizeSettings:
    def __init__(self,
                 title=None,
                 subplots=True,
                 colors=None,
                 subtitles=None,
                 x_axes=None,
                 y_axes=None,
                 grid=False):
        self.title = title
        self.colors = colors
        self.subplots = subplots
        self.subplots = subplots
        self.subtitles = subtitles
        self.x_axes = x_axes
        self.y_axes = y_axes
        self.grid = grid
